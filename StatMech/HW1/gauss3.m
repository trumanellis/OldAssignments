function [g,dg,d2g] = gauss3(x,y);
% computing the third gaussian in the Mueller potential
g = -170*exp(-6.5*(x+0.5)*(x+0.5)+11*(x+0.5)*(y-1.5)-6.5*(y-1.5)*(y-1.5));
dg(1)=(-13*(x+0.5)+11*(y-1.5))*g;
dg(2)=(-13*(y-1.5)+11*(x+0.5))*g;
d2g(1,1)=((-13*(x+0.5)+11*(y-1.5))^2-13)*g;
d2g(2,2)=((-13*(y-1.5)+11*(x+0.5))^2-13)*g;
d2g(1,2)=(11+(-13*(x+0.5)+11*(y-1.5))*(-13*(y-1.5)+11*(x+0.5)))*g;
d2g(2,1)=d2g(1,2);